public class Boss {
    private int helth;
    private String defence;
    private int damag;


    public int getHelth() {
        return helth;
    }
    public void setHelth(int helth) {
        this.helth = helth;
    }

    public String getDefence() {
        return defence;
    }
    public void setDefence(String defence) {
        this.defence = defence;
    }
    public int getDamag() {
        return damag;
    }
    public void setDamag(int damag) {
        this.damag = damag;
    }
}

